    private void cargaTabla(){
        String headers[] = {"Id","nombre","Responsable"};
        DefaultTableModel dtm = new DefaultTableModel(headers,0);
        DaoDepartamento daod = new DaoDepartamento();
        List<Departamento> lista  = daod.obtenDepartamentos();
        if(lista.size() == 0){
            return;
        }
        Object fila[] = new Object[3];
        for(Departamento d : lista){
            fila[0] = d.getId();
            fila[1] = d.getNombre();
            fila[2] = d.getResponsable();
            dtm.addRow(fila);
        }
        tblDepartamentos.setModel(dtm);
    }
