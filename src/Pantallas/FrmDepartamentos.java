private void limpiar(){
    lblIdDepto.setText("");
    txtNombre.setText("");
    txtResponsable.setText("");
    btnModificar.setEnabled(false);
    btnGuardar.setEnabled(true);
    txtNombre.requestFocus();
}
    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        Departamento dep = new Departamento();
        dep.setNombre(txtNombre.getText());
        dep.setResponsable(txtResponsable.getText());
        DaoDepartamento daodep = new DaoDepartamento();
        if(daodep.insertaDepartamento(dep)){
            JOptionPane.showMessageDialog(this, "Departamento insertado!!");
            cargaTabla();
          limpiar();
            
        }else{
            JOptionPane.showMessageDialog(this, "Error al insertar!!");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void tblDepartamentosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDepartamentosMouseClicked
        // TODO add your handling code here:
        int renglon = tblDepartamentos.getSelectedRow();
        lblIdDepto.setText(tblDepartamentos.getValueAt(renglon, 0).toString());
        txtNombre.setText(tblDepartamentos.getValueAt(renglon, 1).toString());
        txtResponsable.setText(tblDepartamentos.getValueAt(renglon, 2).toString());
        btnModificar.setEnabled(true);
        btnGuardar.setEnabled(false);
    }//GEN-LAST:event_tblDepartamentosMouseClicked

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        DaoDepartamento daodep = new DaoDepartamento();
        Departamento depto = new Departamento();
        depto.setId(Integer.parseInt(lblIdDepto.getText()));
        depto.setNombre(txtNombre.getText());
        depto.setResponsable(txtResponsable.getText());
        if (daodep.modifica(depto)) {
            JOptionPane.showMessageDialog(this, "Departamento Modificado!!");
            limpiar();
            cargaTabla();
        }
       
    }//GEN-LAST:event_btnModificarActionPerformed

